parece que mucha gente no usa composer para instalar Kumbia

es tan facil como
composer create-project kumbia/framework mi-dir-de-app

y si quieren pueden escoger la version
composer create-project kumbia/framework:1.1.5 mi-dir-de-app

o la ultima version de la 1.2
composer create-project kumbia/framework:^1.2 mi-dir-de-app